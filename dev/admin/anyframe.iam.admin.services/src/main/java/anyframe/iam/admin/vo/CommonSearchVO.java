package anyframe.iam.admin.vo;

import anyframe.common.util.SearchVO;

/**
 * An Object that contains common search conditions
 * @author AnyframeGen
 * 
 */
public class CommonSearchVO extends SearchVO {
	private static final long serialVersionUID = 1L;

	private int page;

	private int rows;

	private String sidx;

	private String sord;

	public String getSidx() {
		return sidx;
	}

	public void setSidx(String sidx) {
		this.sidx = sidx;
	}

	public String getSord() {
		return sord;
	}

	public void setSord(String sord) {
		this.sord = sord;
	}

	public int getPage() {
		return page;
	}

	public void setPage(int page) {
		this.page = page;
	}

	public int getRows() {
		return rows;
	}

	public void setRows(int rows) {
		this.rows = rows;
	}

}
